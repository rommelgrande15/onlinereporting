function sampleSize(letter){
	if (letter == 'A') {
		return 2;
	}else if (letter == 'B') {
		return 3;
	}else if (letter == 'C') {
		return 5;
	}else if (letter == 'D') {
		return 8;
	}else if (letter == 'E') {
		return 13;
	}else if (letter == 'F') {
		return 20;
	}else if (letter == 'G') {
		return 32;
	}else if (letter == 'H') {
		return 50;
	}else if (letter == 'J') {
		return 80;
	}else if (letter == 'K') {
		return 125;
	}else if (letter == 'L') {
		return 200;
	}else if (letter == 'M') {
		return 315;
	}else if (letter == 'N') {
		return 500;
	}else if (letter == 'P') {
		return 800;
	}else if (letter == 'Q') {
		return 1250;
	}else if (letter == 'R') {
		return 2000;
	}

}
